    private void EditarFuncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarFuncActionPerformed
        // Bot√£o Editar Funcionario

        try {

            indice = TabelaFunc.getSelectedRow();

            if (indice >= 0 && !cpfCadFunc.getText().isEmpty()) {
                mestre.funcionarios.get(indice).nome = nomeCadFunc.getText();
                mestre.funcionarios.get(indice).cpf = cpfCadFunc.getText();
                mestre.funcionarios.get(indice).cargo = (String) cargoCadFunc.getSelectedItem();
                mestre.funcionarios.get(indice).salario = Double.parseDouble(salarioCadFunc.getText());
                mestre.funcionarios.get(indice).dataNasc = dataNascCadFunc.getDateFormatString();
                mestre.funcionarios.get(indice).telefone = telefoneCadFunc.getText();
                mestre.funcionarios.get(indice).email = emailCadFunc.getText();
                mestre.funcionarios.get(indice).foto = fotoCadFunc.getIcon();

                JOptionPane.showMessageDialog(null, "Funcion√°rio Editado com Sucesso!!!");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Selecione o Funcion√°rio na Tabela e Preencha Todos os Campos Corretamente!!!");
        }
    }//GEN-LAST:event_EditarFuncActionPerformed

