    private void PesquisarFuncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PesquisarFuncActionPerformed
        // Bot√£o Pesquisar Funcion√°rio

        DefaultTableModel TF = (DefaultTableModel) TabelaFunc.getModel();
        TF.setNumRows(0);

        for (int i = 0; i < mestre.funcionarios.size(); i++) {

            System.out.println(mestre.funcionarios.get(i).toString());

            if (pesquisarFunc.getText().equalsIgnoreCase("[aA-zZ]")) {
                JOptionPane.showMessageDialog(null, "Digite um CPF!!!");
            }

            if (mestre.funcionarios.get(i).cpf.equals(pesquisarFunc.getText())) {
                TF.setNumRows(0);

                TF.addRow(new Object[]{
                    mestre.funcionarios.get(i).nome,
                    mestre.funcionarios.get(i).cpf,
                    mestre.funcionarios.get(i).salario,});

                //Funcion√°rio
                nomeFunc.setText(mestre.funcionarios.get(i).nome);
                cpfFunc.setText(mestre.funcionarios.get(i).cpf);
                cargoFunc.setText(mestre.funcionarios.get(i).cargo);
                salarioFunc.setText(String.valueOf(mestre.funcionarios.get(i).salario));
                dataNascFunc.setText(mestre.funcionarios.get(i).dataNasc);
                telefoneFunc.setText(mestre.funcionarios.get(i).telefone);
                emailFunc.setText(mestre.funcionarios.get(i).email);

                fotoFunc.setIcon(mestre.funcionarios.get(i).foto);

                //Controle
                nomeCadFunc.setText(mestre.funcionarios.get(i).nome);
                cpfCadFunc.setText(mestre.funcionarios.get(i).cpf);
                cargoCadFunc.setSelectedItem(mestre.funcionarios.get(i).cargo);
                salarioCadFunc.setText(String.valueOf(mestre.funcionarios.get(i).salario));
                dataNascCadFunc.setDateFormatString(mestre.funcionarios.get(i).dataNasc);
                telefoneCadFunc.setText(mestre.funcionarios.get(i).telefone);
                emailCadFunc.setText(mestre.funcionarios.get(i).email);

                fotoCadFunc.setIcon(new ImageIcon(getClass().getResource("/Imagens/Perfil.png")));
                fotoCadFunc.setIcon((Icon) mestre.funcionarios.get(i).foto);

                //Recibo
                nomeEmpresaRecFunc.setText("Empresa Pikorosa de Exercicios");
                salarioRecFunc.setText(String.valueOf(mestre.funcionarios.get(i).salario));
                cargoRecFunc.setText(mestre.funcionarios.get(i).cargo);
            }

            if (pesquisarFunc.getText().equalsIgnoreCase("")) {
                TF.addRow(new Object[]{
                    mestre.funcionarios.get(i).nome,
                    mestre.funcionarios.get(i).cpf,
                    mestre.funcionarios.get(i).salario,});
            }
        }

    }//GEN-LAST:event_PesquisarFuncActionPerformed

    private void CadastrarFuncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarFuncActionPerformed
        // Bot√£o Cadastrar
        try {
            Funcionario F = new Funcionario();

            F.nome = nomeCadFunc.getText();
            F.cpf = cpfCadFunc.getText();
            F.cargo = (String) cargoCadFunc.getSelectedItem();
            F.salario = Double.parseDouble(salarioCadFunc.getText());
            F.dataNasc = dataNascCadFunc.getDateFormatString();
            F.telefone = telefoneCadFunc.getText();
            F.email = emailCadFunc.getText();
            F.foto = fotoCadFunc.getIcon();

            mestre.funcionarios.add(F);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Preencha Todos os Campos Corretamente!!!");
        }

    }//GEN-LAST:event_CadastrarFuncActionPerformed

